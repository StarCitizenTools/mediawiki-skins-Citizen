/*
 * generate-font-fallbacks.js
 *
 * Computes metric-matched fallback override descriptors for the bundled
 * Roboto Flex font. The output is a `@font-face` block that, when paired
 * with the real font in a `font-family` stack, makes the swap from system
 * fallback to web font nearly invisible — eliminating the visible "FOUT"
 * flash on first paint.
 *
 * The four override descriptors (`size-adjust`, `ascent-override`,
 * `descent-override`, `line-gap-override`) are computed by reading the web
 * font's metrics and dividing by the calibration target (Arial). Liberation
 * Sans is metric-identical to Arial, so listing it as a second `local()`
 * source is safe — the overrides remain correct for Linux users.
 *
 * Run when the bundled Roboto Flex woff2 files are updated:
 *
 *     npm install --no-save fontkit
 *     node tools/generate-font-fallbacks.js
 *
 * Then paste the emitted block into resources/skins.citizen.styles/fonts.less.
 */

'use strict';

const fontkit = require( 'fontkit' );
const path = require( 'path' );

// Arial metrics. Source: the Arial font's OS/2 + hhea tables, stable across
// the versions shipped by Windows/macOS/iOS/Android. Liberation Sans (Linux)
// is intentionally metric-identical to Arial.
const ARIAL = {
	unitsPerEm: 2048,
	ascent: 1854,
	descent: 434,
	lineGap: 67,
	xHeight: 1062
};

const WEB_FONT_PATH = path.resolve(
	__dirname,
	'../resources/skins.citizen.styles/fonts/RobotoFlex_latin.woff2'
);

const font = fontkit.openSync( WEB_FONT_PATH );

// Use OS/2 typo metrics when the font sets the USE_TYPO_METRICS flag —
// that's what browsers actually use to size line boxes. Otherwise fall back
// to hhea metrics.
const os2 = font[ 'OS/2' ];
const useTypo = os2.fsSelection.useTypoMetrics;
const webAscentRaw = useTypo ? os2.typoAscender : font.hhea.ascent;
const webDescentRaw = useTypo ? os2.typoDescender : font.hhea.descent;
const webLineGapRaw = useTypo ? os2.typoLineGap : font.hhea.lineGap;

const webEm = font.unitsPerEm;
const webAscent = webAscentRaw / webEm;
const webDescent = Math.abs( webDescentRaw ) / webEm;
const webLineGap = webLineGapRaw / webEm;
const webXHeight = os2.xHeight / webEm;

const arialXHeight = ARIAL.xHeight / ARIAL.unitsPerEm;

// `size-adjust` scales the fallback so its x-height matches the web font.
// This is Capsize's approach — it keeps lowercase letters visually the same
// size on swap, which is what the eye notices most. Line breaks may shift
// slightly because horizontal density still differs, but a size jump is far
// more disruptive visually than minor reflow.
const sizeAdjust = webXHeight / arialXHeight;

// The line-box overrides are expressed relative to the *adjusted* em, so we
// divide the web font's natural em ratio by sizeAdjust to compensate.
const ascentOverride = webAscent / sizeAdjust;
const descentOverride = webDescent / sizeAdjust;
const lineGapOverride = webLineGap / sizeAdjust;

// Match Citizen's stylelint config: no trailing zeros after the decimal point.
const pct = ( n ) => ( n * 100 ).toFixed( 2 ).replace( /\.?0+$/, '' ) + '%';

const block = `// fallback for Roboto, calibrated against Arial / Liberation Sans
// Generated by tools/generate-font-fallbacks.js — do not hand-edit
@font-face {
	font-family: 'Roboto-fallback';
	src: local( 'Arial' ), local( 'Liberation Sans' );
	size-adjust: ${ pct( sizeAdjust ) };
	ascent-override: ${ pct( ascentOverride ) };
	descent-override: ${ pct( descentOverride ) };
	line-gap-override: ${ pct( lineGapOverride ) };
}`;

process.stdout.write( block + '\n' );
